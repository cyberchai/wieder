// Stable vivid color from a seed string, so each anonymous set keeps its own
// hue across renders instead of flickering. Mid lightness keeps the white
// smiley readable on top.
function colorFromSeed(seed: string): string {
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    hash = seed.charCodeAt(i) + ((hash << 5) - hash);
    hash |= 0;
  }
  return `hsl(${Math.abs(hash) % 360}, 62%, 45%)`;
}

